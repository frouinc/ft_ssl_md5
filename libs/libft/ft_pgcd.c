int			ft_pgcd(int a, int b)
{
	int		x;

	if (a < 1 || b < 1)
		return (0);
	if (a > b)
		x = b;
	else
		x = a;
	++x;
	while (--x > 0)
		if ((a % x) == 0 && (b % x) == 0)
			return (x);
	return (0);
}
