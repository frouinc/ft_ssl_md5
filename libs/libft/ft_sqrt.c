#include "libft.h"

int			ft_sqrt(int nb)
{
	int	odd;
	int	i;
	int	sum;

	i = 1;
	sum = 0;
	odd = -1;
	while ((sum += (odd += 2)) < nb)
		i++;
	return (i);
}
